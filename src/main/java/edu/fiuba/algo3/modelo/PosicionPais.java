package edu.fiuba.algo3.modelo;

public class PosicionPais {
    private String nombre;
    private int posX;
    private int posY;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosy(int posY) {
        this.posY = posY;
    }
}
